package com.example.kotlinbasics

fun main(){
    var e = 2.23f
    println(e)
    //round of in 7th number
    e = 2.34342343455534f
    println(e)

    var f = 2.333334
    println(f)
    /*
    * double is more precise than using float
    * it doesn't round the number
    * using var is mutable
    * */

    f = 2.23432432432242334324
    println(f)




}